package com.easyschools.teacher;

public class EventData
{
    private String name , time , desc , day_no , day_txt ;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getDay_no() {
        return day_no;
    }

    public void setDay_no(String day_no) {
        this.day_no = day_no;
    }

    public String getDay_txt() {
        return day_txt;
    }

    public void setDay_txt(String day_txt) {
        this.day_txt = day_txt;
    }
}
