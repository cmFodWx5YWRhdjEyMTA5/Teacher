public class BehaviorData
{
    String icon , title ,  grade , id , total_of_behavior ,degree;

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }


    public String getTotal_of_behavior() {
        return total_of_behavior;
    }

    public void setTotal_of_behavior(String total_of_behavior) {
        this.total_of_behavior = total_of_behavior;
    }

    public String getDegree() {
        return degree;
    }

    public void setDegree(String degree) {
        this.degree = degree;
    }
}
